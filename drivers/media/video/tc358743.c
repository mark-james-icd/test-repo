static int h2c_read_reg(struct i2c_client *client, u16 addr, u8 *val)
{
	int err;
	struct i2c_msg msg[2];
	unsigned char data[3];

	if (!client->adapter)
		return -ENODEV;

	msg[0].addr = client->addr;
	msg[0].flags = 0;
	msg[0].len = 2;
	msg[0].buf = data;

	/* high byte goes out first */
	data[0] = (u8) (addr >> 8);;
	data[1] = (u8) (addr & 0xff);

	msg[1].addr = client->addr;
	msg[1].flags = I2C_M_RD;
	msg[1].len = 1;
	msg[1].buf = data + 2;

	err = i2c_transfer(client->adapter, msg, 2);

	if (err != 2)

		return -EINVAL;

	*val = data[2];

	return 0;
}

static int h2c_write_reg(struct i2c_client *client, u16 addr, u8 val)
{
	int err;
	struct i2c_msg msg;
	unsigned char data[3];
	int retry = 0;

	if (!client->adapter)
		return -ENODEV;

	data[0] = (u8) (addr >> 8);
	data[1] = (u8) (addr & 0xff);
	data[2] = (u8) (val & 0xff);

	msg.addr = client->addr;
	msg.flags = 0;
	msg.len = 3;
	msg.buf = data;

	do {
		err = i2c_transfer(client->adapter, &msg, 1);
		if (err == 1)
			return 0;
		retry++;
		pr_err("h2c: i2c transfer failed, retrying %x %x %d\n",
		       addr, val, err);

		msleep(3);
	} while (retry <= H2C_MAX_RETRIES);

	return err;
}

static int h2c_write_reg_16(struct i2c_client *client, u16 addr, u16 val)
{
	int err;
	struct i2c_msg msg;
	unsigned char data[4];
	int retry = 0;

	if (!client->adapter)
		return -ENODEV;

	data[0] = (u8) (addr >> 8);
	data[1] = (u8) (addr & 0xff);
	data[2] = (u8) (val >> 8);
	data[3] = (u8) (val & 0xff);

	msg.addr = client->addr;
	msg.flags = 0;
	msg.len = 4;
	msg.buf = data;

	do {
		err = i2c_transfer(client->adapter, &msg, 1);
		if (err == 1)
			return 0;

		retry++;
		pr_err("h2c: i2c transfer failed, retrying %x %x %d\n",
		       addr, val, err);

		msleep(3);
	} while (retry <= H2C_MAX_RETRIES);

	return err;
}

static int h2c_write_table(struct i2c_client *client,
			         const struct h2c_32_reg table[])
{
	int err;
	const struct h2c_32_reg *next;
	u8 val;

	for (next = table; next->addr != H2C_TABLE_END; next++) {
		if (next->addr == H2C_TABLE_WAIT_MS) {
			msleep(next->value);
			continue;
		}
		
		if ( next->addr == H2C_TABLE_REPEAT ) {
			int regcnt =  next->value >> 12; // how many registers to write repeatedly
			int rptcnt =  next->value & 0xfff; // how many times to repeat
			
			int c,n;
			const struct h2c_32_reg * rpt;
			for (c = 0; c < rptcnt; c++) {
  				for ( n = 0, rpt = next + 1; n < regcnt; n++, rpt++ ) {
					err = h2c_write_reg(client, rpt->addr, rpt->value);
					if (err) {
						pr_err("%s: write error @%d\n", __func__, 
							rpt - table);
						return err;
					}
				}
			}
			next += regcnt;
			continue;
		}

		val = next->value;

		err = h2c_write_reg(client, next->addr, val);
		if (err)
			return err;
	}
	return 0;
}



